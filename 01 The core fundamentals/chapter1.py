# Conversation starters: expressions, names, and print

# Expressions evaluate to values (not stored unless assigned)
2 + 3 * 4       # 14
"hi".upper()    # 'HI'

# Statements do actions (assignment, def, import)
name = "Navneet"

# Printing shows results explicitly
print("Hello,", name)

